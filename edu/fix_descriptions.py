name_to_id = {
    "東暦（E暦）": "東暦",
    "A-Registry（A籍）": "A-Registry",
    "V7（Vital Seven）": "V7",
}

def main():
    with open(FILEPATH, 'r', encoding='utf-8') as f:
        lines = f.readlines()

    in_terminology = False
    current_id = None
    modifications = 0
    found_ids = set()

    # Build a lookup by id: for each id in expansions, find the expansion
    # Also handle name-to-id mapping
    id_to_expansion = {}
    for key, value in expansions.items():
        if key in name_to_id:
            actual_id = name_to_id[key]
            id_to_expansion[actual_id] = value
        else:
            id_to_expansion[key] = value

    i = 0
    while i < len(lines):
        line = lines[i]

        if 'const TERMINOLOGY' in line:
            in_terminology = True

        if in_terminology and re.match(r'^];', line):
            in_terminology = False
            current_id = None

        if in_terminology:
            # Track current id
            m = re.search(r'id:\s*"([^"]+)"', line)
            if m:
                current_id = m.group(1)

            if current_id and current_id in id_to_expansion:
                new_desc = id_to_expansion[current_id]

                # Case 1: description: "text" on same line (inline)
                desc_inline = re.search(r'description:\s*"([^"]*)"', line)
                if desc_inline and not re.match(r'^\s*description:\s*$', line):
                    old_desc = desc_inline.group(1)
                    if len(old_desc) < 100:
                        old_quoted = f'"{old_desc}"'
                        new_quoted = f'"{new_desc}"'
                        lines[i] = line.replace(old_quoted, new_quoted, 1)
                        modifications += 1
                        found_ids.add(current_id)
                        print(f"  [inline] {current_id}: {len(old_desc)} -> {len(new_desc)} chars")
                        current_id = None  # prevent double-processing

                # Case 2: description: on this line, string on next line
                elif re.match(r'^\s*description:\s*$', line):
                    i += 1
                    if i < len(lines):
                        next_line = lines[i]
                        m3 = re.match(r'^(\s*)"([^"]*)"(,?)\s*$', next_line)
                        if m3:
                            indent = m3.group(1)
                            old_desc = m3.group(2)
                            trailing = m3.group(3)
                            if len(old_desc) < 100:
                                lines[i] = f'{indent}"{new_desc}"{trailing}\n'
                                modifications += 1
                                found_ids.add(current_id)
                                print(f"  [multiline] {current_id}: {len(old_desc)} -> {len(new_desc)} chars")
                                current_id = None

        i += 1

    # Write back
    with open(FILEPATH, 'w', encoding='utf-8') as f:
        f.writelines(lines)

    print(f"\n--- Summary ---")
    print(f"Modified {modifications} entries out of {len(id_to_expansion)} expansion targets")
    if found_ids:
        print(f"Successfully expanded IDs: {sorted(found_ids)}")
    not_found = set(id_to_expansion.keys()) - found_ids
    if not_found:
        print(f"IDs not found in file: {sorted(not_found)}")

if __name__ == '__main__':
    main()
